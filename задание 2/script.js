function plus () {
    let a = prompt('Введите первое число');
    let b = prompt('Введите второе число');
    alert ( `А это результат сложения: ${+a + +b}` )
}

function minus () {
    let a = prompt('Введите первое число');
    let b = prompt('Введите второе число');
    alert ( `А это результат вычитания: ${a - b}` )
}

function times () {
    let a = prompt('Введите первое число');
    let b = prompt('Введите второе число');
    alert ( `А это результат умножения: ${a * b}` )
}

function divide () {
    let a = prompt('Введите первое число');
    let b = prompt('Введите второе число');
    alert ( `А это результат деления: ${a / b}` )
}